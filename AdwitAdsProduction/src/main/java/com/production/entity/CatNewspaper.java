package com.production.entity;

public class CatNewspaper {
	private int id;
	private int client;
	private int publication;
	private String name;
	private String initials;
	private String slugType;
	private int helpDesk;
	private int team;
	private int businessGroupId;
	private int channel;
	private int status;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getClient() {
		return client;
	}

	public void setClient(int client) {
		this.client = client;
	}

	public int getPublication() {
		return publication;
	}

	public void setPublication(int publication) {
		this.publication = publication;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getInitials() {
		return initials;
	}

	public void setInitials(String initials) {
		this.initials = initials;
	}

	public String getSlugType() {
		return slugType;
	}

	public void setSlugType(String slugType) {
		this.slugType = slugType;
	}

	public int getHelpDesk() {
		return helpDesk;
	}

	public void setHelpDesk(int helpDesk) {
		this.helpDesk = helpDesk;
	}

	public int getTeam() {
		return team;
	}

	public void setTeam(int team) {
		this.team = team;
	}

	public int getBusinessGroupId() {
		return businessGroupId;
	}

	public void setBusinessGroupId(int businessGroupId) {
		this.businessGroupId = businessGroupId;
	}

	public int getChannel() {
		return channel;
	}

	public void setChannel(int channel) {
		this.channel = channel;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

}
