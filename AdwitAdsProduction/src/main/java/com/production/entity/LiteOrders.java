package com.production.entity;

public class LiteOrders {
	private int id;
	private int uid;
	private String jobName;
	private float width;
	private float height;
	private int colorPreference;
	private String dateNeeded;
	private float credits;
	private String copyContent;
	private String instruction;
	private int status;
	private String timestamp;
	private String docImage;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public String getJobName() {
		return jobName;
	}

	public void setJobName(String jobName) {
		this.jobName = jobName;
	}

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}

	public int getColorPreference() {
		return colorPreference;
	}

	public void setColorPreference(int colorPreference) {
		this.colorPreference = colorPreference;
	}

	public String getDateNeeded() {
		return dateNeeded;
	}

	public void setDateNeeded(String dateNeeded) {
		this.dateNeeded = dateNeeded;
	}

	public float getCredits() {
		return credits;
	}

	public void setCredits(float credits) {
		this.credits = credits;
	}

	public String getCopyContent() {
		return copyContent;
	}

	public void setCopyContent(String copyContent) {
		this.copyContent = copyContent;
	}

	public String getInstruction() {
		return instruction;
	}

	public void setInstruction(String instruction) {
		this.instruction = instruction;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public String getDocImage() {
		return docImage;
	}

	public void setDocImage(String docImage) {
		this.docImage = docImage;
	}

}
